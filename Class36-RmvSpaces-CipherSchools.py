name = "     Sujal Bochkar     "
dots = "................"
# strip() method removes the 

print(name + dots) #do nothing
print(name.lstrip() + dots) #removes left space in the string
print(name.rstrip() + dots) #removes right space in the string
print(name.strip() + dots) # removes all the spaces but not btw the string
print(name.replace(" ","") + dots)